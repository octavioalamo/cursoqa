Cypress.Commands.add('getExistingUser', () => {
    cy.task("connectDB", "select email from users order by random() limit 1").then(row => {
        const email = row
        cy.task("connectDB", `update users set active=true where email='${email}'`).then( () => {
            return email;
        })
    })
})